const getFilterSql = (filters) => {
  let filterSql = " where 1=1 ";

  if (filters.user.value) {
    filterSql = filterSql + ` and user like '%${filters.user.value}%'`;
  }

  if (filters.documentId.value) {
    filterSql =
      filterSql + ` and documentId like'%${filters.documentId.value}%'`;
  }
  if (filters.summary.value) {
    filterSql = filterSql + ` and summary like '%${filters.summary.value}%'`;
  }
  if (filters.notes.value) {
    filterSql = filterSql + ` and notes like '%${filters.notes.value}%'`;
  }

  if (filters.summaryGeneratedOn.value) {
    filterSql =
      filterSql +
      ` and summaryGeneratedOn like '%${filters.summaryGeneratedOn.value}%'`;
  }

  return filterSql;
};

const getOrderSql = (sortField, sortOrder) => {
  return `order by  ${sortField} ${sortOrder == 1 ? "asc" : "desc"}`;
};

const getLimitQuery = (skipCount, takeCount) => {
  return ` limit ${skipCount} , ${takeCount}`;
};

const filterTableData = (array, filters) => {
  let filteredArray = array;

  if (filters.user.value) {
    filteredArray = filteredArray.filter((summarizerData) =>
      summarizerData.user
        .toLowerCase()
        .includes(filters.user.value.toLowerCase())
    );
  }

  if (filters.documentId.value) {
    filteredArray = filteredArray.filter((summarizer) =>
      summarizer.documentId
        .toLowerCase()
        .includes(filters.documentId.value.toLowerCase())
    );
  }
  if (filters.notes.value) {
    filteredArray = filteredArray.filter((summarizer) =>
      summarizer.notes.toLowerCase().includes(filters.notes.value.toLowerCase())
    );
  }
  if (filters.summary.value) {
    filteredArray = filteredArray.filter((summarizer) =>
      summarizer.summary
        .toLowerCase()
        .includes(filters.summary.value.toLowerCase())
    );
  }
  if (filters.summaryGeneratedOn.value) {
    filteredArray = filteredArray.filter((summarizer) =>
      summarizer.summaryGeneratedOn
        .toLowerCase()
        .includes(filters.summaryGeneratedOn.value.toLowerCase())
    );
  }

  return filteredArray;
};

const sortByProperty = (array, property, direction = 1) => {
  // Check if direction is 'asc' or 'desc'
  const sortOrder = direction;

  // Sorting the array based on the specified property and direction
  return array.sort((a, b) => {
    // Compare the values of the specified property
    const valueA = a[property];
    const valueB = b[property];

    if (valueA < valueB) return -1 * sortOrder;
    if (valueA > valueB) return 1 * sortOrder;
    return 0;
  });
};

